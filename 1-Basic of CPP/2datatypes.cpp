//This is the program for datatypes knowledge
#include <iostream>
using namespace std;
int main(){
    int x = 10;
    short s = 2;
    float y = 3.05;
    double d = 987652;
    long l = 9888787;
    char ch = 'A';
    string str = "Hello";
    cout<<"The integer value - "<<x<<endl<<"The float value - "<<y<<endl<<"The short value - "<<s<<endl<<"The double value - "<<d<<endl<<"The long value - "<<l<<endl<<"The char value - "<<ch<<endl<<"The string value - "<<str<<endl;
return 0;
}


